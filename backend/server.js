app.post("/auth/login", asyncHandler(async (req, res) => {
  const { email, password } = req.body;
  if (!email || !password) {
    return res.status(400).json({ error: "Email and password are required" });
  }

  const result = await pool.query("SELECT * FROM users WHERE email = $1", [email]);

  if (result.rows.length === 0) return res.status(400).json({ error: "User not found" });

  const user = result.rows[0];
  if (!(await bcrypt.compare(password, user.password))) return res.status(401).json({ error: "Invalid credentials" });

  const token = jwt.sign({ id: user.id, email: user.email }, SECRET, { expiresIn: "1h" });

  // –ü–æ–ª—É—á–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ–± —É—Å—Ç—Ä–æ–π—Å—Ç–≤–µ –∏ –±—Ä–∞—É–∑–µ—Ä–µ
  const device = req.useragent.platform;
  const browser = req.useragent.browser;
  const ipAddress = req.ip;

  // –°–æ–∑–¥–∞–µ–º –∑–∞–ø–∏—Å—å –æ –≤—Ö–æ–¥–µ
  try {
    await pool.query(
      "INSERT INTO login_logs (user_id, device, browser, login_time, token, ip_address) VALUES ($1, $2, $3, NOW(), $4, $5)",
      [user.id, device, browser, token, ipAddress]
    );

    res.cookie("token", token, { httpOnly: true, secure: process.env.NODE_ENV === "production", sameSite: "Strict" });
    res.json({ message: "Login successful", user });
  } catch (error) {
    console.error("–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –∑–∞–ø–∏—Å–∏ –æ –≤—Ö–æ–¥–µ:", error);
    res.status(500).json({ error: "Internal server error" });
  }
}));

