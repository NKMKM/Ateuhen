// üîπ –£–¥–∞–ª–∏—Ç—å —Å–µ—Å—Å–∏—é (–≤—ã—Ö–æ–¥ —Å —É—Å—Ç—Ä–æ–π—Å—Ç–≤–∞)
app.delete("/auth/sessions/:sessionId", asyncHandler(async (req, res) => {
  const token = req.cookies.token;
  if (!token) return res.status(401).json({ message: "Not authenticated" });

  try {
    const decoded = jwt.verify(token, SECRET);
    const userId = decoded.id;
    const sessionId = req.params.sessionId;

    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ª–∏ —Å–µ—Å—Å–∏—è
    const checkSession = await pool.query(
      "SELECT * FROM login_logs WHERE id = $1 AND user_id = $2",
      [sessionId, userId]
    );

    if (checkSession.rows.length === 0) {
      return res.status(404).json({ error: "Session not found" });
    }

    // –£–¥–∞–ª—è–µ–º —Å–µ—Å—Å–∏—é (—É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º token –≤ NULL)
    const result = await pool.query(
      "UPDATE login_logs SET token = NULL WHERE id = $1 AND user_id = $2 RETURNING token",
      [sessionId, userId]
    );

    const deletedSessionToken = result.rows[0].token;

    // –ï—Å–ª–∏ —É–¥–∞–ª–µ–Ω–∞ —Ç–µ–∫—É—â–∞—è —Å–µ—Å—Å–∏—è, –≤—ã–∫–∏–¥—ã–≤–∞–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    if (deletedSessionToken === token) {
      res.clearCookie("token", { 
        httpOnly: true, 
        secure: process.env.NODE_ENV === "production", 
        sameSite: "Strict",
        domain: "localhost",
        path: "/"
      });

      // –£–≤–µ–¥–æ–º–ª—è–µ–º –∫–ª–∏–µ–Ω—Ç–∞ —á–µ—Ä–µ–∑ WebSocket
      io.emit("forceLogout", userId);

      return res.status(200).json({ message: "Session deleted. You have been logged out." });
    }

    res.status(200).json({ message: "Session deleted" });
  } catch (error) {
    console.error("–û—à–∏–±–∫–∞ –ø—Ä–∏ —É–¥–∞–ª–µ–Ω–∏–∏ —Å–µ—Å—Å–∏–∏:", error);
    res.status(500).json({ error: "Internal server error", details: error.message });
  }
}));

// üîπ Middleware –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç–∏ —Å–µ—Å—Å–∏–∏
app.use(asyncHandler(async (req, res, next) => {
  const token = req.cookies.token;
  if (token) {
    try {
      const decoded = jwt.verify(token, SECRET);

      // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ª–∏ —Ç–æ–∫–µ–Ω –≤ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö
      const session = await pool.query(
        "SELECT * FROM login_logs WHERE token = $1 AND user_id = $2",
        [token, decoded.id]
      );

      if (session.rows.length === 0) {
        res.clearCookie("token", { 
          httpOnly: true, 
          secure: process.env.NODE_ENV === "production", 
          sameSite: "Strict", 
          domain: "localhost",
          path: "/"
        });
        return res.status(401).json({ message: "Session expired" });
      }

      req.user = decoded;
    } catch (err) {
      res.clearCookie("token", { 
        httpOnly: true, 
        secure: process.env.NODE_ENV === "production", 
        sameSite: "Strict",
        domain: "localhost",
        path: "/"
      });
      return res.status(401).json({ message: "Invalid token" });
    }
  }
  next();
}));

